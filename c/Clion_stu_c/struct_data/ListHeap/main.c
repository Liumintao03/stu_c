#include "ListHeap.h"

int main(){
    //创建
    HeapList *heapList = createListHeapInit();
    //插入
    for(int i = 1;i<=10;i++){
        addListHeap(heapList,&i);
    }
    //打印
    printHeapListinfo(heapList);
    //出队
    int n = heapList->count;
    for(int i = 1;i<=n;i++){
        popListHeap(heapList);

    }

    //打印
    printHeapListinfo(heapList);
    //释放
    releaseHeapList(heapList);



    return 0;
}